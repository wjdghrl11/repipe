# 3단 구구단
# 반복 숙달, while문 중첩을 이해

count = 0
while count < 3 :
  dan = int(input('원하는 단을 입력해주세요 : '))
  num = 1
  while num <= 9 :
    print('{} * {} = {}' .format(dan, num, dan * num))
    num += 1
count += 1





